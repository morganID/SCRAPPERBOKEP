def _domain_to_slug(domain: str) -> str:
    """jable.tv → jable_tv"""
    return re.sub(r"[^a-zA-Z0-9]", "_", domain.replace("www.", ""))


def _url_to_domain(url: str) -> str:
    """https://www.jable.tv/categories/... → jable.tv"""
    if not url.startswith(("http://", "https://")):
        url = "https://" + url
    return urlparse(url).netloc.replace("www.", "")


def find_adapter_file(domain: str) -> Optional[Path]:
    """Find adapter .py file for domain."""
    slug = _domain_to_slug(domain)
    path = ADAPTERS_FOLDER / f"{slug}.py"
    if path.exists():
        return path
    # Also try with www
    slug_www = _domain_to_slug("www." + domain)
    path_www = ADAPTERS_FOLDER / f"{slug_www}.py"
    if path_www.exists():
        return path_www
    return None


def load_adapter_class(filepath: Path) -> Optional[Dict]:
    """
    Parse adapter .py file via regex.
    Simple and reliable — no dynamic import needed.
    """
    return _parse_adapter_text(filepath)


def _patch_adapter_imports(module_name: str):
    """
    Create dummy base classes so adapter files can be imported
    without the full package structure.
    """
    # Create fake parent package
    class DummyBaseAdapter:
        pass
    
    class DummyRegistry:
        @staticmethod
        def register(cls):
            return cls
    
    # Fake modules
    parent_name = module_name.rsplit(".", 1)[0] if "." in module_name else "csv_getter_adapters"
    
    class FakeModule:
        BaseAdapter = DummyBaseAdapter
        AdapterRegistry = DummyRegistry
    
    # Register in sys.modules so "from .. import" resolves
    sys.modules.setdefault("csv_getter", FakeModule())
    sys.modules.setdefault("csv_getter.adapters", FakeModule())
    sys.modules.setdefault("csv_getter.adapters.domains", FakeModule())


def _parse_adapter_text(filepath: Path) -> Optional[Dict]:
    """
    Parse adapter .py as text to extract selectors.
    Handles quotes inside selectors like [class*='thumb']
    """
    text = filepath.read_text(encoding="utf-8")
    selectors = {}

    fields = {
        "container":    "CONTAINER_SELECTOR",
        "title":        "TITLE_SELECTOR",
        "link":         "LINK_SELECTOR",
        "thumbnail":    "THUMBNAIL_SELECTOR",
        "duration":     "DURATION_SELECTOR",
        "views":        "VIEWS_SELECTOR",
        "pag_type":     "PAGINATION_TYPE",
        "pag_selector": "PAGINATION_SELECTOR",
        "next_btn":     "NEXT_BUTTON_SELECTOR",
        "loadmore":     "LOAD_MORE_SELECTOR",
        "sentinel":     "SENTINEL_SELECTOR",
        "page_param":   "PAGE_PARAM",
        "api_endpoint": "API_ENDPOINT",
    }

    for key, attr in fields.items():
        # Try double quotes first (handles single quotes inside)
        #   THUMBNAIL_SELECTOR = "[class*='thumb'] img"
        #                         ^^^^^^^^^^^^^^^^^^^^^ captured
        pattern_dq = attr + r'\s*=\s*"([^"]*)"'
        match = re.search(pattern_dq, text)

        if not match:
            # Then try single quotes (handles double quotes inside)
            pattern_sq = attr + r"\s*=\s*'([^']*)'"
            match = re.search(pattern_sq, text)

        if match:
            selectors[key] = match.group(1)

    # Domain
    domain_match = re.search(r'DOMAINS\s*=\s*\[(.+?)\]', text)
    if domain_match:
        domains = re.findall(r'["\']([^"\']+)["\']', domain_match.group(1))
        selectors["domains"] = domains

    if selectors.get("container"):
        return selectors

    return None

def _adapter_to_config(adapter, base_url: str) -> "SiteConfig":
    """Convert loaded adapter (class or dict) to SiteConfig."""
    
    if isinstance(adapter, dict):
        # From text parsing
        d = adapter
        domain = d.get("domains", [""])[0] if d.get("domains") else _url_to_domain(base_url)
        
        return SiteConfig(
            base_url=base_url,
            domain=domain,
            container=d.get("container", "article"),
            title=d.get("title", ".title"),
            link=d.get("link", "a"),
            thumbnail=d.get("thumbnail", "img"),
            duration=d.get("duration", ".duration"),
            views=d.get("views", ".views"),
            pagination_type=d.get("pag_type", "numbered"),
            pagination_selector=d.get("pag_selector", ".pagination a"),
            next_button_selector=d.get("next_btn", ""),
            load_more_selector=d.get("loadmore", ""),
            sentinel_selector=d.get("sentinel", ""),
            page_param=d.get("page_param", "page"),
            api_endpoint=d.get("api_endpoint", ""),
        )
    
    # From class
    cls = adapter
    domain = ""
    if hasattr(cls, "DOMAINS") and cls.DOMAINS:
        domain = cls.DOMAINS[0]
    if not domain:
        domain = _url_to_domain(base_url)
    
    pag_type = getattr(cls, "PAGINATION_TYPE", "numbered")
    
    return SiteConfig(
        base_url=base_url,
        domain=domain,
        container=getattr(cls, "CONTAINER_SELECTOR", "article"),
        title=getattr(cls, "TITLE_SELECTOR", ".title"),
        link=getattr(cls, "LINK_SELECTOR", "a"),
        thumbnail=getattr(cls, "THUMBNAIL_SELECTOR", "img"),
        duration=getattr(cls, "DURATION_SELECTOR", ".duration"),
        views=getattr(cls, "VIEWS_SELECTOR", ".views"),
        pagination_type=pag_type,
        pagination_selector=getattr(cls, "PAGINATION_SELECTOR", ".pagination a"),
        next_button_selector=getattr(cls, "NEXT_BUTTON_SELECTOR", ""),
        load_more_selector=getattr(cls, "LOAD_MORE_SELECTOR", ""),
        sentinel_selector=getattr(cls, "SENTINEL_SELECTOR", ""),
        page_param=getattr(cls, "PAGE_PARAM", "page"),
        api_endpoint=getattr(cls, "API_ENDPOINT", ""),
    )


