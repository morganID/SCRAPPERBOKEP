    target_url = sys.argv[1]
    max_pg = int(sys.argv[2]) if len(sys.argv) > 2 else 0
    conc = int(sys.argv[3]) if len(sys.argv) > 3 else 5
    
    asyncio.run(run(target_url, concurrent=conc, max_pages=max_pg))