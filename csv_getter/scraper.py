class CSVGetter:
    """
    Scrape video listings to CSV/{domain}.csv
    
    Uses adapter files saved by Scout for selectors.
    """
    
    def __init__(self, config: SiteConfig):
        self.config = config
        self._playwright: Optional[Playwright] = None
        self._browser: Optional[Browser] = None
        self._context: Optional[BrowserContext] = None
        self._all_data: List[Dict] = []
        self._seen_links: set = set()
    
    async def __aenter__(self):
        await self._start()
        return self
    
    async def __aexit__(self, *args):
        await self._close()
    
    async def _start(self):
        CSV_FOLDER.mkdir(parents=True, exist_ok=True)
        self._playwright = await async_playwright().start()
        self._browser = await self._playwright.chromium.launch(
            headless=True, args=BROWSER_ARGS,
        )
        self._context = await self._browser.new_context(
            user_agent=random.choice(USER_AGENTS),
            viewport={"width": 1920, "height": 1080},
            locale="en-US",
            bypass_csp=True,
            ignore_https_errors=True,
        )
        await self._context.add_init_script(STEALTH_JS)
    
    async def _close(self):
        if self._browser:
            await self._browser.close()
        if self._playwright:
            await self._playwright.stop()
    
    # ── URL builder ──
    
    def _build_page_url(self, page_num: int) -> str:
        base = self.config.base_url.rstrip("/")
        cfg = self.config
        
        if page_num <= 1:
            return base + "/"
        
        if cfg.page_in_path:
            clean = re.sub(r"/page/\d+/?$", "", base)
            return f"{clean}/page/{page_num}/"
        else:
            parsed = urlparse(base)
            params = parse_qs(parsed.query)
            params[cfg.page_param] = [str(page_num)]
            return urlunparse(parsed._replace(
                query=urlencode(params, doseq=True)
            ))
    
    # ── Extract ──
    
    async def _extract_from_page(self, page: Page) -> List[Dict]:
        cfg = self.config
        return await page.evaluate(
            """(s) => {
                const results = [];
                document.querySelectorAll(s.container).forEach(item => {
                    const t = item.querySelector(s.title);
                    const l = item.querySelector(s.link);
                    const i = item.querySelector(s.thumbnail);
                    const d = item.querySelector(s.duration);
                    const v = item.querySelector(s.views);
                    
                    const title = t ? t.textContent.trim()
                                    : (l ? l.textContent.trim() : '');
                    const link = l ? (l.href || l.getAttribute('data-href') || '') : '';
                    const thumbnail = i
                        ? (i.src || i.getAttribute('data-src')
                           || i.getAttribute('data-lazy-src')
                           || i.getAttribute('data-original') || '')
                        : '';
                    const duration = d ? d.textContent.trim() : '';
                    const views = v ? v.textContent.trim() : '';
                    
                    if (title || link) {
                        results.push({ title, link, thumbnail, duration, views });
                    }
                });
                return results;
            }""",
            {
                "container": cfg.container,
                "title":     cfg.title,
                "link":      cfg.link,
                "thumbnail": cfg.thumbnail,
                "duration":  cfg.duration,
                "views":     cfg.views,
            },
        )
    
    # ── Scroll ──
    
    async def _scroll_down(self, page: Page, times: int = 3):
        for _ in range(times):
            await page.evaluate(
                f"window.scrollBy(0, {random.randint(400, 900)})"
            )
            await asyncio.sleep(0.8 + random.random() * 0.5)
        await page.evaluate("window.scrollTo(0, document.body.scrollHeight)")
        await asyncio.sleep(1)
    
    # ── Dedup ──
    
    def _dedup(self, data: List[Dict]) -> List[Dict]:
        new_items = []
        for item in data:
            link = item.get("link", "")
            if link and link not in self._seen_links:
                self._seen_links.add(link)
                new_items.append(item)
            elif not link:
                title = item.get("title", "")
                key = f"__t__{title}"
                if title and key not in self._seen_links:
                    self._seen_links.add(key)
                    new_items.append(item)
        return new_items
    
    # ── Detect total pages ──
    
    async def _detect_total_pages(self) -> int:
        cfg = self.config
        
        if cfg.pagination_type in ("load_more", "infinite_scroll"):
            return 0
        
        page = await self._context.new_page()
        try:
            url = self._build_page_url(1)
            await page.goto(url, wait_until="domcontentloaded", timeout=45_000)
            await asyncio.sleep(2)
            
            total = await page.evaluate(
                """(pagSel) => {
                    let max = 1;
                    const links = document.querySelectorAll(pagSel);
                    links.forEach(a => {
                        const hm = (a.href||'').match(/\\/page\\/(\\d+)/);
                        if (hm) max = Math.max(max, parseInt(hm[1]));
                        const qm = (a.href||'').match(/[?&]page=(\\d+)/);
                        if (qm) max = Math.max(max, parseInt(qm[1]));
                        const text = a.textContent.trim();
                        if (/^\\d+$/.test(text)) max = Math.max(max, parseInt(text));
                    });
                    // "Page X of Y"
                    const ofM = document.body.innerText.match(/page\\s+\\d+\\s+of\\s+(\\d+)/i);
                    if (ofM) max = Math.max(max, parseInt(ofM[1]));
                    // data attributes
                    const dEl = document.querySelector('[data-total-pages],[data-last-page]');
                    if (dEl) {
                        const v = dEl.getAttribute('data-total-pages') || dEl.getAttribute('data-last-page');
                        if (v && /^\\d+$/.test(v)) max = Math.max(max, parseInt(v));
                    }
                    return max;
                }""",
                cfg.pagination_selector,
            )
            return total or 1
        except Exception as exc:
            logger.warning(f"⚠️ Can't detect pages: {exc}")
            return 1
        finally:
            await page.close()
    
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    #  Scrape Strategies
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    
    async def _scrape_single_page(self, page_num: int) -> Tuple[int, List[Dict]]:
        page = await self._context.new_page()
        try:
            url = self._build_page_url(page_num)
            resp = await page.goto(url, wait_until="domcontentloaded", timeout=45_000)
            if resp and resp.status >= 400:
                logger.warning(f"   ❌ Page {page_num}: HTTP {resp.status}")
                return (page_num, [])
            await asyncio.sleep(1.5 + random.random())
            await self._scroll_down(page, times=2)
            data = await self._extract_from_page(page)
            for item in data:
                item["page"] = page_num
            logger.info(f"   ✅ Page {page_num}: {len(data)} videos")
            return (page_num, data)
        except Exception as exc:
            logger.warning(f"   ❌ Page {page_num}: {str(exc)[:60]}")
            return (page_num, [])
        finally:
            await page.close()
    
    async def _scrape_numbered(self, total_pages: int) -> List[Dict]:
        all_data: List[Dict] = []
        concurrent = self.config.concurrent
        
        for batch_start in range(1, total_pages + 1, concurrent):
            batch_end = min(batch_start + concurrent, total_pages + 1)
            pages = list(range(batch_start, batch_end))
            logger.info(f"📦 Batch: pages {batch_start}–{batch_end - 1}")
            
            tasks = [self._scrape_single_page(pg) for pg in pages]
            results = await asyncio.gather(*tasks)
            
            for _, data in results:
                new = self._dedup(data)
                all_data.extend(new)
            
            if all_data:
                self._save_csv(all_data, temp=True)
            
            await asyncio.sleep(1.5 + random.random())
        
        return all_data
    
    async def _scrape_click_next(self) -> List[Dict]:
        cfg = self.config
        selector = cfg.next_button_selector or cfg.pagination_selector
        all_data: List[Dict] = []
        page_num = 0
        max_pages = cfg.max_pages or 500
        
        page = await self._context.new_page()
        try:
            await page.goto(
                self._build_page_url(1),
                wait_until="domcontentloaded", timeout=45_000,
            )
            await asyncio.sleep(2)
            
            while page_num < max_pages:
                page_num += 1
                await self._scroll_down(page, times=2)
                
                data = await self._extract_from_page(page)
                for item in data:
                    item["page"] = page_num
                
                new = self._dedup(data)
                all_data.extend(new)
                logger.info(f"   ✅ Page {page_num}: {len(new)} new videos (total {len(all_data)})")
                
                if new:
                    self._save_csv(all_data, temp=True)
                
                btn = page.locator(selector).first
                try:
                    if await btn.count() == 0 or not await btn.is_visible():
                        logger.info("   🏁 No more 'Next' button — done")
                        break
                    await btn.click()
                    await page.wait_for_load_state("domcontentloaded", timeout=30_000)
                    await asyncio.sleep(2 + random.random())
                except Exception:
                    logger.info("   🏁 Next click failed — done")
                    break
        finally:
            await page.close()
        
        return all_data
    
    async def _scrape_load_more(self) -> List[Dict]:
        cfg = self.config
        selector = cfg.load_more_selector
        all_data: List[Dict] = []
        click_count = 0
        max_clicks = cfg.max_pages or 200
        no_new = 0
        
        page = await self._context.new_page()
        try:
            await page.goto(
                self._build_page_url(1),
                wait_until="domcontentloaded", timeout=45_000,
            )
            await asyncio.sleep(2)
            
            while click_count < max_clicks:
                await self._scroll_down(page, times=2)
                
                data = await self._extract_from_page(page)
                for item in data:
                    item["page"] = click_count + 1
                
                new = self._dedup(data)
                if new:
                    all_data.extend(new)
                    no_new = 0
                    logger.info(f"   ✅ Click {click_count}: +{len(new)} (total {len(all_data)})")
                    self._save_csv(all_data, temp=True)
                else:
                    no_new += 1
                    if no_new >= 3:
                        logger.info("   🏁 No new data 3× — done")
                        break
                
                btn = page.locator(selector).first
                try:
                    if await btn.count() == 0 or not await btn.is_visible():
                        logger.info("   🏁 Load-more gone — done")
                        break
                    await btn.scroll_into_view_if_needed()
                    await btn.click()
                    click_count += 1
                    await asyncio.sleep(2 + random.random())
                except Exception:
                    logger.info("   🏁 Load-more failed — done")
                    break
        finally:
            await page.close()
        
        return all_data
    
    async def _scrape_infinite_scroll(self) -> List[Dict]:
        all_data: List[Dict] = []
        scroll_count = 0
        max_scrolls = self.config.max_pages or 100
        no_new = 0
        
        page = await self._context.new_page()
        try:
            await page.goto(
                self._build_page_url(1),
                wait_until="domcontentloaded", timeout=45_000,
            )
            await asyncio.sleep(2)
            
            while scroll_count < max_scrolls:
                data = await self._extract_from_page(page)
                for item in data:
                    item["page"] = scroll_count + 1
                
                new = self._dedup(data)
                if new:
                    all_data.extend(new)
                    no_new = 0
                    logger.info(f"   ✅ Scroll {scroll_count}: +{len(new)} (total {len(all_data)})")
                    self._save_csv(all_data, temp=True)
                else:
                    no_new += 1
                    if no_new >= 5:
                        logger.info("   🏁 No new data 5 scrolls — done")
                        break
                
                prev_h = await page.evaluate("document.documentElement.scrollHeight")
                await page.evaluate("window.scrollTo(0, document.body.scrollHeight)")
                scroll_count += 1
                await asyncio.sleep(2.5 + random.random())
                new_h = await page.evaluate("document.documentElement.scrollHeight")
                if new_h == prev_h:
                    no_new += 1
        finally:
            await page.close()
        
        return all_data
    
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    #  CSV Save
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    
    def _save_csv(self, data: List[Dict], temp: bool = False) -> Path:
        CSV_FOLDER.mkdir(parents=True, exist_ok=True)
        path = self.config.csv_temp_path if temp else self.config.csv_path
        
        fieldnames = ["no", "page", "title", "link", "thumbnail", "duration", "views"]
        rows = []
        for i, item in enumerate(data, 1):
            rows.append({
                "no": i,
                "page": item.get("page", ""),
                "title": item.get("title", ""),
                "link": item.get("link", ""),
                "thumbnail": item.get("thumbnail", ""),
                "duration": item.get("duration", ""),
                "views": item.get("views", ""),
            })
        
        with open(path, "w", newline="", encoding="utf-8-sig") as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(rows)
        
        return path
    
    def _cleanup_temp(self):
        try:
            self.config.csv_temp_path.unlink(missing_ok=True)
        except Exception:
            pass
    
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    #  Main scrape()
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    
    async def scrape(self) -> List[Dict]:
        cfg = self.config
        ptype = cfg.pagination_type
        
        print(f"\n{'=' * 58}")
        print(f"🚀  CSV GETTER v3")
        print(f"{'=' * 58}")
        cfg.print_summary()
        print(f"{'─' * 58}")
        
        if ptype in ("numbered", "url_pattern"):
            total = await self._detect_total_pages()
            if cfg.max_pages:
                total = min(total, cfg.max_pages)
            print(f"\n📊 {total} pages detected")
            print(f"🔄 {cfg.concurrent} concurrent\n")
            all_data = await self._scrape_numbered(total)
        
        elif ptype == "next_button":
            print(f"\n➡️ Next-button: {cfg.next_button_selector}\n")
            all_data = await self._scrape_click_next()
        
        elif ptype == "load_more":
            print(f"\n📥 Load-more: {cfg.load_more_selector}\n")
            all_data = await self._scrape_load_more()
        
        elif ptype == "infinite_scroll":
            print(f"\n♾️ Infinite scroll\n")
            all_data = await self._scrape_infinite_scroll()
        
        else:
            print(f"\n⚠️ Unknown type '{ptype}' — trying single page\n")
            _, data = await self._scrape_single_page(1)
            all_data = self._dedup(data)
        
        all_data.sort(key=lambda x: x.get("page", 0))
        final_path = self._save_csv(all_data, temp=False)
        self._cleanup_temp()
        
        print(f"\n{'=' * 58}")
        print(f"🎉  DONE!")
        print(f"{'─' * 58}")
        print(f"   Total videos: {len(all_data)}")
        print(f"   Saved to:     {final_path}")
        print(f"{'=' * 58}\n")
        
        return all_data


