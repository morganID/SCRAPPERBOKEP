class SiteConfig:
    """All config needed to scrape one site."""
    
    def __init__(
        self,
        base_url: str,
        domain: str = "",
        container: str = "article",
        title: str = ".title",
        link: str = "a",
        thumbnail: str = "img",
        duration: str = ".duration",
        views: str = ".views",
        pagination_type: str = "numbered",
        pagination_selector: str = ".pagination a",
        next_button_selector: str = "",
        load_more_selector: str = "",
        sentinel_selector: str = "",
        page_param: str = "page",
        page_in_path: bool = True,
        api_endpoint: str = "",
        concurrent: int = DEFAULT_CONCURRENT,
        max_pages: int = 0,
    ):
        self.base_url = base_url
        self.domain = domain or _url_to_domain(base_url)
        self.container = container
        self.title = title
        self.link = link
        self.thumbnail = thumbnail
        self.duration = duration
        self.views = views
        self.pagination_type = pagination_type
        self.pagination_selector = pagination_selector
        self.next_button_selector = next_button_selector
        self.load_more_selector = load_more_selector
        self.sentinel_selector = sentinel_selector
        self.page_param = page_param
        self.page_in_path = page_in_path
        self.api_endpoint = api_endpoint
        self.concurrent = concurrent
        self.max_pages = max_pages
    
    @property
    def domain_slug(self) -> str:
        return _domain_to_slug(self.domain)
    
    @property
    def csv_path(self) -> Path:
        return CSV_FOLDER / f"{self.domain_slug}.csv"
    
    @property
    def csv_temp_path(self) -> Path:
        return CSV_FOLDER / f"{self.domain_slug}_temp.csv"
    
    @classmethod
    def from_adapter_file(cls, filepath: Path, base_url: str) -> "SiteConfig":
        """Load config from saved adapter .py file."""
        adapter = load_adapter_class(filepath)
        if adapter is None:
            adapter = _parse_adapter_text(filepath)
        if adapter is None:
            raise ValueError(f"Cannot parse adapter file: {filepath}")
        return _adapter_to_config(adapter, base_url)
    
    @classmethod
    def from_url(cls, url: str) -> Optional["SiteConfig"]:
        """
        Try to find existing adapter file for this URL's domain.
        Returns None if no adapter found.
        """
        domain = _url_to_domain(url)
        filepath = find_adapter_file(domain)
        if filepath:
            return cls.from_adapter_file(filepath, url)
        return None
    
    def print_summary(self):
        """Print config summary."""
        print(f"   Domain:      {self.domain}")
        print(f"   URL:         {self.base_url}")
        print(f"   Container:   {self.container}")
        print(f"   Title:       {self.title}")
        print(f"   Link:        {self.link}")
        print(f"   Thumbnail:   {self.thumbnail}")
        print(f"   Duration:    {self.duration}")
        print(f"   Views:       {self.views}")
        print(f"   Pagination:  {self.pagination_type}")
        if self.pagination_type == "next_button":
            print(f"   Next btn:    {self.next_button_selector}")
        elif self.pagination_type == "load_more":
            print(f"   Load more:   {self.load_more_selector}")
        elif self.pagination_type == "infinite_scroll":
            print(f"   Sentinel:    {self.sentinel_selector}")
        else:
            print(f"   Pag select:  {self.pagination_selector}")
        print(f"   Output:      {self.csv_path}")


