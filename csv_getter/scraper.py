#  🎯 Main Entry Point
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


async def run(
    url: str,
    concurrent: int = DEFAULT_CONCURRENT,
    max_pages: int = 0,
    headless: bool = True,
    proxy: Optional[str] = None,
    force_scout: bool = False,
) -> List[Dict]:
    """
    Main entry point. Just give a URL.
    
    1. Check if adapter exists for this domain
    2. If not → run Scout → save adapter
    3. Load adapter → scrape → save CSV/{domain}.csv
    
    Args:
        url:          Target URL
        concurrent:   Parallel page fetches (for numbered pagination)
        max_pages:    Limit pages (0 = no limit)
        headless:     Browser headless mode
        proxy:        Optional proxy server
        force_scout:  Re-run scout even if adapter exists
    """
    domain = _url_to_domain(url)
    
    print(f"\n{'=' * 58}")
    print(f"🎯  CSV GETTER")
    print(f"{'=' * 58}")
    print(f"   URL:    {url}")
    print(f"   Domain: {domain}")
    
    # ── Step 1: Find or create adapter ──
    
    adapter_path = find_adapter_file(domain)
    
    if adapter_path and not force_scout:
        print(f"   ✅ Adapter found: {adapter_path}")
        config = SiteConfig.from_adapter_file(adapter_path, url)
    else:
        if force_scout:
            print(f"   🔄 Force re-scan requested")
        else:
            print(f"   ❌ No adapter for '{domain}'")
        
        print(f"\n{'─' * 58}")
        print(f"🔍  Running Scout to analyze site...")
        print(f"{'─' * 58}")
        
        # Import scout
        from .scout import CSVScout, print_report, generate_adapter_code
        
        async with CSVScout(proxy=proxy, headless=headless) as scout:
            analysis = await scout.analyze(url)
        
        print_report(analysis)
        
        if analysis.error:
            print(f"\n❌ Scout failed: {analysis.error}")
            return []
        
        if not analysis.is_valid:
            print("\n⚠️ Scout couldn't find valid selectors")
            print("   Run scout manually: python -m csv_getter.csv_scout <url>")
            return []
        
        # Save adapter file
        code = generate_adapter_code(analysis)
        ADAPTERS_FOLDER.mkdir(parents=True, exist_ok=True)
        adapter_path = ADAPTERS_FOLDER / f"{_domain_to_slug(domain)}.py"
        adapter_path.write_text(code, encoding="utf-8")
        print(f"\n💾 Adapter saved: {adapter_path}")
        
        # Load the config from what we just saved
        config = SiteConfig.from_adapter_file(adapter_path, url)
    
    # ── Step 2: Apply overrides ──
    
    config.concurrent = concurrent
    config.max_pages = max_pages
    
    # ── Step 3: Scrape ──
    
    print(f"\n{'─' * 58}")
    print(f"📥  Starting scrape...")
    print(f"{'─' * 58}")
    
    async with CSVGetter(config) as getter:
        data = await getter.scrape()
    
    return data


