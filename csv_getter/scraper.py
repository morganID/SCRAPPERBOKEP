#  CLI  (python -m csv_getter.csv_getter <url>)
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

if __name__ == "__main__":
    import sys
    
    logging.basicConfig(
        level=logging.INFO,
        format="%(message)s",
    )
    
    if len(sys.argv) < 2:
        print("Usage: python -m csv_getter.csv_getter <url> [max_pages] [concurrent]")
        print("Example: python -m csv_getter.csv_getter https://jable.tv/categories/chinese/ 10 5")
